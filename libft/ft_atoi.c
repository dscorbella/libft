#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	menos;
	int	result;

	i = 0;
	result = 0;
	menos = 1;
	while (str[i] == 32 || (str[i] > 8 && str[i] < 14))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{	
		if (str[i] == '-')
			menos = -menos;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	return (result * menos);
}
